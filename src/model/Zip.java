package model;

public class Zip {
	
	private int zipCode;
	private String city;
	
	public Zip(int zipCode, String city) {
		
	}

	public int getZipCode() {
		return zipCode;
	}

	public void setZipCode(int zipCode) {
		this.zipCode = zipCode;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}
	
	
}


